-- MAGNITUDE ANALYSIS
-- Find total customers by country
SELECT
country,
COUNT(customer_key) AS total_customers
FROM dbo.[gold.dim_customers]
GROUP BY country
ORDER BY total_customers DESC

-- Find total customers by gender
SELECT
gender,
COUNT(customer_key) AS total_customers
FROM dbo.[gold.dim_customers]
GROUP BY gender
ORDER BY total_customers DESC

-- Find total products by category
SELECT
category,
COUNT(product_key) AS total_products
FROM dbo.[gold.dim_products]
GROUP BY category
ORDER BY total_products DESC

-- Average costs in each category
SELECT
category,
AVG(cost) AS avg_costs
FROM dbo.[gold.dim_products]
GROUP BY category
ORDER BY avg_costs DESC

-- Total ravenue generated for each category
SELECT 
p.category,
SUM(f.sales_amount) AS total_ravenue
FROM dbo.[gold.fact_sales] f
LEFT JOIN dbo.[gold.dim_products] p
	ON p.product_key = f.product_key
GROUP BY category
ORDER BY total_ravenue DESC

-- Total ravenue generated by each customer
SELECT 
c.customer_key,
c.first_name,
c.last_name,
SUM(f.sales_amount) AS total_ravenue
FROM dbo.[gold.fact_sales] f
LEFT JOIN dbo.[gold.dim_customers] c
	ON c.customer_key = f.customer_key
GROUP BY
c.customer_key,
c.first_name,
c.last_name
ORDER BY total_ravenue DESC

-- Distribution of sold items across countries
SELECT 
c.country,
SUM(f.quantity) AS total_sold_items
FROM dbo.[gold.fact_sales] f
LEFT JOIN dbo.[gold.dim_customers] c
	ON c.customer_id = f.customer_key
GROUP BY
c.country
ORDER BY total_sold_items DESC

