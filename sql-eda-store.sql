-- RANKING ANALYSIS

-- Which 5 products generate the highest ravenue?
SELECT TOP 5
p.product_name,
SUM(f.sales_amount) AS total_ravenue
FROM dbo.[gold.fact_sales] f
LEFT JOIN dbo.[gold.dim_products] p
	ON p.product_key = f.product_key
GROUP BY p.product_name
ORDER BY total_ravenue DESC

-- Which are the 5 worst-performing products in terms of sales?
SELECT TOP 5
p.product_name,
SUM(f.sales_amount) AS total_ravenue
FROM dbo.[gold.fact_sales] f
LEFT JOIN dbo.[gold.dim_products] p
	ON p.product_key = f.product_key
GROUP BY p.product_name
ORDER BY total_ravenue



-- using the window function
SELECT *
FROM (
	SELECT p.product_name,
	SUM(f.sales_amount) AS total_ravenue,
	ROW_NUMBER() OVER(ORDER BY SUM(f.sales_amount) DESC) AS rank_products
	FROM dbo.[gold.fact_sales] f
	LEFT JOIN dbo.[gold.dim_products] p
		ON p.product_key = f.product_key
	GROUP BY p.product_name) t
WHERE rank_products <=5

-- Top 10 customers who have generated the highest ravenue
